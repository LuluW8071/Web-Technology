var firstName = 'Alan';
var lastName = 'Turing';

// var.length specifies len of string defined in a variable
console.log('Length of firstName:', firstName.length, '\nLength of lastName:', lastName.length)

// Index in Js always starts at 0 
console.log(firstName[0], firstName[1], firstName[2], firstName[3])

// Finding last letter in a string
console.log('Last letter of string in firstName:', firstName[firstName.length - 1])
console.log('Last letter of string in lastName:', lastName[lastName.length - 1])