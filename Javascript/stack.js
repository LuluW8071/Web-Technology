// Manipulating array using push() function
var arr = [1, 2, 3, 4, 5];
var stringArr = ['Happy', 'sad'];

arr.push(6);
stringArr.push('Joy');

console.log('Push() function:',arr, stringArr);

// Manipulating array using pop() function
arr.pop();
stringArr.pop();

console.log('Pop() function',arr, stringArr);